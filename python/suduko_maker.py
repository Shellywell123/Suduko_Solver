def make_3x3(elements):
    """ constructs the structure of a 3x3 out of a list of elements"""
    section = [elements[:3],elements[3:6],elements[6:9]]
    return section

        ###############################################################
        
def make_9x9(sections):
    """ constructs the structure for a 9x9 out of 3x3's"""
    suduko = [sections[:3],sections[3:6],sections[6:9]]
    return suduko

