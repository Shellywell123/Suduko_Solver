def print_section(section):
    """ prints a 3x3 suduko given lists within a list structure"""
    vline = '|'
    hline = ' -------------\n'
    for n in [0,1,2]:
        print (hline,vline,section[n][0],vline,section[n][1],vline,section[n][2],vline)
    print(hline)

        ###############################################################
            
def print_suduko(suduko):
    """ prints out 9x9 suduko given list within list structure"""
    vline = '|'
    hline = ' -------------------------------'
    print ('\n')
    for n in [0,1,2]:
        print(hline)
        for k in [0,1,2]:
            print  (
                '',
                vline,suduko[n][0][k][0],
                '',suduko[n][0][k][1],
                '',suduko[n][0][k][2],               
                vline,suduko[n][1][k][0],
                '',suduko[n][1][k][1],
                '',suduko[n][1][k][2],               
                vline,suduko[n][2][k][0],
                '',suduko[n][2][k][1],
                '',suduko[n][2][k][2],
                vline,
                )
    print(hline) 

